public class _3_DataTypes {
    public static void main(String[] args) {
      byte a = 8;
      System.out.println(a);
      char ch = 'a';  
      System.out.println(ch);
      boolean var = false;
      System.out.println(var);
      float pr = 1.5f; 
    // you have to use ending f or (float) as java considers decimal numbers to be double by default
      System.out.println(pr); 
      float price = (float)1.5;
      System.out.println(price);
      // long(large numbers)
      // double(large decimals)
      // short(short numbers, less storage than int, best for performance) 
    }
}
